    private void btnModulesDirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModulesDirActionPerformed
        JFileChooser fileChooser = new JFileChooser();
        fileChooser.setFileSelectionMode(JFileChooser.DIRECTORIES_ONLY);
        if (fileChooser.showDialog(this, "Selecionar pasta") == JFileChooser.APPROVE_OPTION) {
            try (OutputStream output = new FileOutputStream(PROPERTIES_PATH)) {
                Properties prop = new Properties();
                prop.setProperty(PROP_MODULES_PATH, fileChooser.getSelectedFile().getAbsolutePath());
                prop.store(output, null);
                lbModulesDir.setText(fileChooser.getSelectedFile().getAbsolutePath());
            } catch (IOException io) {
                Logger.getLogger(JDialogGenerator.class.getName()).log(Level.SEVERE, null, io);
            }
        }
    }//GEN-LAST:event_btnModulesDirActionPerformed

