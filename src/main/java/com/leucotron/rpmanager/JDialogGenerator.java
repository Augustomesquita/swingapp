    private String getModulesDirFromProperties() {
        String modulesDir = "Nenhum diret贸rio definido...";
        try (InputStream configProperties = new FileInputStream(PROPERTIES_PATH)) {
            Properties prop = new Properties();
            prop.load(configProperties);
            modulesDir = prop.getProperty(PROP_MODULES_PATH);
        } catch (FileNotFoundException ex) {
            try (OutputStream output = new FileOutputStream(PROPERTIES_PATH)) {
                Properties prop = new Properties();
                prop.setProperty(PROP_MODULES_PATH, modulesDir);
                prop.store(output, null);
            } catch (IOException io) {
                Logger.getLogger(JDialogGenerator.class.getName()).log(Level.SEVERE, null, io);
            }
        } catch (IOException ex) {
            Logger.getLogger(JDialogGenerator.class.getName()).log(Level.SEVERE, null, ex);
        }
        return modulesDir;
    }

